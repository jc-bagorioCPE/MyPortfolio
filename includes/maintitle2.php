        <h2 class="section-title">DR YANGA'S COLLEGE INC.</h2>
        <p>Dr. Yanga's College Inc. (DYCI), originally founded as Francisco Balagtas Academy (FBA) in 1950 by Dr. Clemente Yanga, began as a secondary school in Bocaue, Bulacan. Driven by a vision to provide quality education to the local community, the institution steadily grew in reputation and scope. In 1986, it transitioned into a higher education institution, becoming Dr. Yanga’s Francisco Balagtas College before evolving into Dr. Yanga’s College Inc. DYCI expanded its academic offerings to include a wide range of undergraduate and graduate programs in fields such as business, education, engineering, information technology, and health sciences. </p>
        <p>Today, DYCI is celebrated for its commitment to academic excellence, holistic student development, and innovation. The institution continues to uphold high standards through accreditation, modern facilities, and a strong emphasis on values formation and leadership. With a legacy of producing graduates who make significant contributions to their professions and communities, DYCI remains a premier educational institution in Bulacan, shaping the future of its students and the region.</p> 
